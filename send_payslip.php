      <div class="row"><span class="label">Bonus</span><span class="value"><?= fm($p['bonus']) ?></span></div>
      <?php endif; ?>
      <?php foreach ($allows as $a): ?>
      <div class="row"><span class="label"><?= ucfirst($a['allowance_type']) ?> Allowance</span><span class="value"><?= fm($a['amount']) ?></span></div>
      <?php endforeach; ?>
      <?php foreach ($comms as $c): ?>
