      <div class="row"><span class="label">Department</span><span class="value"><?= htmlspecialchars($p['department']) ?></span></div>
      <div class="row"><span class="label">Job Type</span><span class="value"><?= ($p['job_type'] ?? '') === 'remote' ? '🌐 Remote Job' : '🏢 On-site Job' ?></span></div>
      <div class="row"><span class="label">Payment Method</span><span class="value"><?= ucwords(str_replace('_',' ',$p['payment_method'])) ?></span></div>
