<?php
$emailBody = ob_get_clean();

// Send email
$toEmail   = $p['email'];
$toName    = $p['full_name'];
$fromEmail = getSetting('email_from') ?: (getSetting('company_email') ?: 'payroll@creativelements.co');
$fromName  = $companyName;
$subject   = "Payslip for {$period} | {$companyName}";
$msgId     = '<' . time() . '.' . md5($toEmail) . '@creativelements.co>';

$headers  = "MIME-Version: 1.0\r\n";
$headers .= "Content-Type: text/html; charset=UTF-8\r\n";
$headers .= "From: {$fromName} <{$fromEmail}>\r\n";
$ccEmail = getCCEmail();
if ($ccEmail) $headers .= "Cc: {$ccEmail}\r\n";
$headers .= "Reply-To: {$fromEmail}\r\n";
$headers .= "Return-Path: {$fromEmail}\r\n";
$headers .= "Message-ID: {$msgId}\r\n";
$headers .= "Date: " . date('r') . "\r\n";
$headers .= "X-Mailer: PHP/" . phpversion() . "\r\n";
$headers .= "X-Priority: 3\r\n";
$headers .= "X-MSMail-Priority: Normal\r\n";
$headers .= "Importance: Normal";

$params = "-f{$fromEmail}";
$sent = mail($toEmail, $subject, $emailBody, $headers, $params);

if ($sent) {
    try {
        $db->prepare("INSERT INTO payslip_emails (payroll_id, sent_to, sent_at) VALUES (?,?,NOW()) ON DUPLICATE KEY UPDATE sent_at=NOW()")
           ->execute([$payrollId, $toEmail]);
    } catch (Exception $e) { /* ignore */ }
    $ccNote = $ccEmail ? " (CC: {$ccEmail})" : '';
    setFlash('success', "✅ Payslip emailed to {$toName} ({$toEmail}){$ccNote}!");
} else {
    setFlash('error', "❌ Failed to send email to {$toEmail}. Check your cPanel email settings.");
}

header('Location: ' . SITE_URL . '/payslips.php?id=' . $payrollId . '&month=' . date('Y-m', strtotime($p['month'].'-01')));
exit;
